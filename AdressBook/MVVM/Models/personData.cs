namespace AdressBook.MVVM.Models {
    class personData {
        private string firstName, lastName, tel;

        public string FirstName {
            get { return firstName; }
            set { firstName = value; }
        }
        public string LastName {
            get { return lastName; }
            set { lastName = value; }
        }
        public string Tel {
            get { return tel; }
            set { tel = value; }
        }
    }
}
